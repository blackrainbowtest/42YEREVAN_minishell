#include "minishell.h"

void	free_tokens(t_token *lst)
{
	t_token	*tmp;

	while (lst)
	{
		tmp = lst->next;
		free(lst->value);
		lst->value = NULL;
		free(lst);
		lst = tmp;
	}
}
